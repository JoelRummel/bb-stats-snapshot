	<h2>About BayStats</h2>
	<p>Baystats is an evolution from the old "Item Popularity Snapshots" that I occasionally posted to the Battle Bay
	subreddit (and - rarely - the Battle Bay Forums). These snapshots consisted of a text-based report that used the same 
	stats that are visible here. They were generally effective, but there was one major problem: because the data had to 
	be collected from the game by hand, each report took many hours to compile. This meant that I could only provide the 
	community with Item Popularity Snapshots every two or three months! Throw in the new special events that are now
	happening every weekend, and the reports became even less potent.</p>
	
	<h3>So how'd this website come about?</h3>
	<p>I set out to develop a faster, more automated method of data collection. When I finally had this breakthrough, 
	I needed a place to host the information it provided; after all, I couldn't post a new report to the 
	forums and subreddit every single day!</p>
	<p>Thus, BayStats was born. Now, players can keep up-to-date with item popularity at their own pace.</p>
